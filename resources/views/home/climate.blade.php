    <div>
        <p class="text-justify" style="font-size: 15px;">Guarapuava é uma das cidades mais frias do Paraná. Graças a sua altitude
            de mais de 1000m a média das máximas em Janeiro é apenas 26,9°C. Já no mês mais frio, Julho, a média das
            mínimas é 8,5°C. A média anual é aproximadamente 16°C.
            O clima Cfb, mesotérmico sempre úmido, garante chuvas constantes e bem distribuídas ao longo do ano,
            somando quase 2000mm anuais.
            Clima Subtropical Úmido Mesotérmico (Cfb), verões frescos (temperatura média inferior a 22°C), invernos com
            ocorrências de geadas severas e frequentes (temperatura média superior a 3°C e inferior a 18°C), não apresentando estação seca.</p>
