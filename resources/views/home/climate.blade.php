    <div>
        <p class="text-justify" style="font-size: 15px;">
            A estação morna permanece por 5 meses, de 2 de novembro a 5 de abril, com temperatura máxima média diária acima de 24 °C. O dia mais quente do ano é 21 de janeiro, cuja temperatura máxima média é de 25 °C e a mínima média é de 17 °C.
            A estação fresca permanece por 2,7 meses, de 19 de maio a 9 de agosto, com temperatura máxima diária em média abaixo de 19 °C. O dia mais frio do ano é 22 de julho, com média de 8 °C para a temperatura mínima e 18 °C para a máxima.
