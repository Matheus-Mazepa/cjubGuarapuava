            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Parque das crianças</b></h3>
            <p>Local de passeios em família, conta com grande estrutura de parquinhos para as crianças, incluindo cama elástica, tem pista para ciclistas e área para piquenique. Localiza-se na Avenida Manoel Ribas.</p>
