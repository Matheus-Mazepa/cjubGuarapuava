        <div class="col-sm-8">
            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Estátua do Índio Guairacá</b></h3>
            <p>A estátua que mede 2 m de altura, colocada sobre um pedestal de 5 m é uma homenagem aos índios que habitaram toda a região e ao seu lendário cacique Guairacá (lobos dos campos e das águas), intrépido defensor de suas terras, que rechaçava todas as tentativas de domínio por parte do invasor. Localizada na entrada da cidade, na Avenida Manoel Ribas.</p>
        </div>
