        <div class="col-sm-8">
            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Parque Recreativo Municipal do Rio Jordão</b></h3>
            <p>Possui 30 hectares de muito verde e ar puro, proporcionando momentos de descontração aos habitantes e aos turistas, através de seus equipamentos de lazer: área para camping, churrasqueiras, lanchonetes, campo de futebol, cancha reta para corridas de cavalos, pista de motocross, além de toda uma estrutura de esportes náuticos incluindo pedalinhos, balsa, trampolins, e piscinas naturais para adultos e crianças, saltos e quedas d'água. É um local cheio de surpresas, atrações e encantos, que se complementa com as lendas da Gruta do Monge e sua fonte milagrosa. Situa-se a 6 km da cidade, com acesso pela Avenida Rubem Siqueira Ribas.</p>
        </div>
