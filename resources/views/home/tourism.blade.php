        <div class="col-sm-8">
            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Parque do lago</b></h3>
            <p>Cartão postal de Guarapuava, o Parque do Lago tem mais de 20 anos de existência. Recebe visitantes durante o ano todo. Nos dias frios de inverno, muitas pessoas vão ao local para tomar chimarrão e aproveitam para registrar as cerejeiras floridas. Ainda, é comum ver muitas pessoas fazendo piqueniques, caminhadas e praticando esportes do parque. Ele possui uma pista de skate, quadra poliesportiva, em que os visitantes jogam futebol, futebol de areia, basquete e vôlei. E desde o ano passado é possível realizar esportes aquáticos no lago, como stand up pedal, remo e caiaque.</p>
