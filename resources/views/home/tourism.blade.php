        <div class="col-sm-8">
            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Parque Municipal das Araucárias</b></h3>
            <p>Reserva ecológica que conta com aproximadamente 3.800 araucárias angustifolia preservadas, árvores nativas e fauna rica e variada. Ocupa uma área de 100 hectares. Conta também com um centro de visitantes onde é realizada atividades de educação ambiental. O parque abriga ainda: Museu de Ciências Naturais de Guarapuava e o recém-inaugurado Jardim Europeu.</p>
        </div>
    </div>
