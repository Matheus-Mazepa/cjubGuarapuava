        <div class="col-sm-8">
            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Lagoa das Lágrimas</b></h3>
            <p>Formação natural encontrada na área central da cidade e que hoje é utilizada como praça de lazer, com pedalinho, pista de Cooper e quadras de esportes. É procurada por toda a população, sendo um dos mais pitorescos lugares para visita.</p>
        </div>
    </div>
