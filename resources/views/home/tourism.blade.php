        <div class="col-sm-8">
            <h3 style="width: 100%; text-align: center; font-size: 20px;"><b>Catedral de Nossa Senhora de Belém</b></h3>
            <p>A construção é caracterizada pelos seus belos afrescos. Além de ser um marco da fundação da cidade, o destaque desta igreja é o seu acervo e que consta uma imagem originária de Portugal mandada buscar pela esposa de um dos pioneiros de Guarapuava, o Tenente-Brigadeiro Antonio de Rocha Loures. Localiza-se na Rua Senador Pinheiro Machado.</p>
