    <label style="width: 100%; text-align: center; font-size: 40px;">Turismo em Guarapuava</label>
    <div>
        <p class="text-justify" style="font-size: 15px;">Nossa cidade tem amplas opções de lazer. Atualmente a prefeitura está desenvolvendo um projeto chamado “Guarapuava Turística”, para que no ano de 2026 o município se torne um polo de turismo. Separamos os pontos de possível visita no domingo a tarde:
    </div>
